function getBuildingText(objID) {
    switch (objID.toLocaleLowerCase()) {
        case "city hall":
            return cityHall;
        case "church":
            return church;
        case "archerstower":
            return archersTower;
        case "farm":
            return farm;
        case "woodcutters camp":
            return woodcutters_camp;
        case "barracks":
            return barracks;
        case "quarry":
            return quarry;
        case "fishing port":
            return fishing_port;
        default:
            return;
    }
}

